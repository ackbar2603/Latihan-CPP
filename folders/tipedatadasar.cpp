#include <iostream>
#include <string>

using namespace std;

int main(){
    //int untuk angka bulat
    int a = 10;
    
    //string untuk kalimat
    string nama = "Hello Ikmal Akbar";

    //float untuk angka desimal
    float berat = 10.5;

    //double untuk angka desimal lebih besar
    double pi = 3.14159265358979323846;

    //char untuk karakter tunggal
    char huruf = 'A';

    //bool untuk nilai benar atau salah
    bool isTrue = true;
    bool isFalse = false;

    //menampilkan hasil
    cout << "Nilai a: " << a << endl;
    cout << "Nama: " << nama << endl;
    cout << "Berat: " << berat << endl;
    cout << "Pi: " << pi << endl;
    cout << "Huruf: " << huruf << endl;
    cout << "isTrue: " << isTrue << endl;
    cout << "isFalse: " << isFalse << endl;
    cout << "----------------------------------------" << endl;
}